    private void loadTable(){
        DefaultTableModel model = (DefaultTableModel) tblDauDiem.getModel();
        model.setNumRows(0);
        List<QuanLyDauDiem> listDauDiems = this.dauDiemService.findAllView();
        for(QuanLyDauDiem dd : listDauDiems){
            Object[] objects = new Object[]{
                dd.getMa(),dd.getTen()
            };
            tblDauDiem.addRow(objects);
        }
    }
    private QuanLyDauDiem validateDauDiem(){
        String ma = Validatetor.kiemTraRong(txtMa, "Ma khong duoc trong");
        if(ma == null) return null;
        String ten = Validatetor.kiemTraRong(txtTen, "Ten khong duoc trong");
        if(ten == null) return null;
        QuanLyDauDiem dauDiem = new QuanLyDauDiem(ma, ten);
        return dauDiem;
    }
