package com.nhomsau.viewmodel;

/**
 *
 * @author Admin
 */
public class ManageSubject {

    private String code;
    private String name;

    public ManageSubject() {
    }

    public ManageSubject(String code, String name) {
        this.code = code;
        this.name = name;
    }

    public String getCode() {
        return code;
    }

    public void setCode(String code) {
        this.code = code;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    @Override
    public String toString() {
        return code + " - " + name;
    }

}
