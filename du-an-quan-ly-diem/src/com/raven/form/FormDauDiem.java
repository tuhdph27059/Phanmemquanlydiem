    private void btnThemActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnThemActionPerformed
        // TODO add your handling code here:
        QuanLyDauDiem dauDiem = this.validateDauDiem();
        if(dauDiem != null){
           int confirm = JOptionPane.showConfirmDialog(this, "Ban co muon them khong?");
           if(confirm == JOptionPane.YES_OPTION){
               this.dauDiemService.insert(dauDiem);
               loadTable();
           }
        }
    }//GEN-LAST:event_btnThemActionPerformed

    private void btnSuaActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSuaActionPerformed
        // TODO add your handling code here:
        int selectedRow = tblDauDiem.getSelectedRow();
        if(selectedRow != -1){
            QuanLyDauDiem dauDiem = this.validateDauDiem();
            if(dauDiem != null){
               int confirm = JOptionPane.showConfirmDialog(this, "Ban co muon sua khong?");
               if(confirm == JOptionPane.YES_OPTION){
                   this.dauDiemService.update(dauDiem);
                   loadTable();
               }
            }
        }
    }//GEN-LAST:event_btnSuaActionPerformed

    private void btnXoaActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnXoaActionPerformed
        // TODO add your handling code here:
        int selectedRow = tblDauDiem.getSelectedRow();
        if(selectedRow != -1){
            String ma = (String) tblDauDiem.getValueAt(selectedRow, 0);
            int confirm = JOptionPane.showConfirmDialog(this, "Ban co muon xoa khong?");
               if(confirm == JOptionPane.YES_OPTION){
                   this.dauDiemService.delete(ma);
                   loadTable();
               }
        }
    }//GEN-LAST:event_btnXoaActionPerformed

